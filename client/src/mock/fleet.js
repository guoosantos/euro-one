export function filterByTenant(list, tenantId) {
  return list.filter((item) => !tenantId || item.tenantId === tenantId || item.tenantId == null);
}

export function summariseFleet(tenantId) {
  const fleet = filterByTenant(vehicles, tenantId);
  const online = fleet.filter((v) => v.status === "online").length;
  const alert = fleet.filter((v) => v.status === "alert").length;
  const offline = fleet.filter((v) => v.status === "offline").length;
  const camerasOk = fleet.filter((v) => v.cameraStatus === "ok").length;
  const blocked = fleet.filter((v) => v.alerts?.includes("panic")).length;
  return {
    total: fleet.length,
    online,
    alert,
    offline,
    camerasOk,
    blocked,
  };
}

export function buildVehicleTable(tenantId) {
  return filterByTenant(vehicles, tenantId).map((v) => ({
    id: v.id,
    name: v.name,
    plate: v.plate,
    status: v.status,
    lastUpdate: v.lastUpdate,
    address: v.address,
    speed: v.speed,
    ignition: v.ignition,
    battery: v.battery,
    signal: v.signal,
    satellites: v.satellites,
    alerts: v.alerts,
  }));
}
